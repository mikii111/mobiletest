package com.example.classes.main;

public class Modifierare {
	public static double plus(double first, double sec){
		double sum = first + sec;
		return sum;
	}
	public static double div(double first, double sec){
		double sum = first / sec;
		return sum;
	}
	public static double min(double first, double sec){
		double sum = first - sec;
		return sum;
	}
	public static double multi(double first, double sec){
		double sum = first * sec;
		return sum;
	}
}
